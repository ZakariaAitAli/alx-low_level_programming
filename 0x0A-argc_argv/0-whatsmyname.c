#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: 'program that prints its name'
 * @argc: number of argument
 * @argv: array of pointers to the args
 *
 * Return: Always 0 (Success)
 */
int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
